# LOOPS

# FOR LOOP
people = ['John', 'Sara', 'Tim', 'Bob']
for person in people:
    print(person)
    
# Iterate by seq index
for i in range(len(people)):
    print(people[i])
    
for i in range(0, 10):
    print(i)
    

# WHILE LOOP

count = 0
while count < 10:
    print("Count:", count)
    count = count + 1
