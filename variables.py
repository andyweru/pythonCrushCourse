# Data type

myStr = 'Hello'
myInt = 23
myFloat = 1.3

myList = [1,2,3,'Hello']
myDict = {'a': 1, 'b': 2, 'c': 3}

print(type(myStr), myStr)
print(type(myInt), myInt)
print(type(myFloat), myFloat)
print(type(myList), myList)
print(type(myDict), myDict)